    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usuario = txtUsuario.getText();
        String contraseña = txtContraseña.getText();

        if (!usuario.isEmpty() && !contraseña.isEmpty()) {
            if (usuario.equals("admin") && contraseña.equals("123")) {
                JOptionPane.showMessageDialog(this, "¡Inicio de sesión exitoso!");
                if (loginListener != null) {
                    loginListener.onLoginSuccess();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nombre de usuario o contraseña inválidos. Por favor, intente de nuevo.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, complete ambos campos de usuario y contraseña.");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

